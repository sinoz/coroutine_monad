import { expect } from "chai"
import { describe, it } from "mocha"

describe('Coroutine constructors', function() {
    it('succeed', function() {
      expect(3 * 5).equal(15);
    });

    it('suspend', function() {
      expect(3 * 5).equal(15);
    });

    it('fail', function() {
      expect(3 * 5).equal(15);
    });

    it('effect', function() {
      expect(3 * 5).equal(15);
    });

    it('transform', function() {
      expect(3 * 5).equal(15);
    });

    it('compute', function() {
      expect(3 * 5).equal(15);
    });

    it('fromOption', function() {
      expect(3 * 5).equal(15);
    });

    it('fromEither', function() {
      expect(3 * 5).equal(15);
    });
});